package day02;

public class FloatExample {
	public static void main(String[] args) {
		float f1 = 7.12345f;
		double d1 = 7.12345;
		System.out.println(f1);
		System.out.println(d1);
		
		float f2 = 1.234567891234f;
		double d2 = 1.234567891234;
		System.out.println("\n" + f2);
		System.out.println(d2);
	}
}